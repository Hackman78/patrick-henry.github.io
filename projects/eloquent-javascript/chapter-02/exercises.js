function drawChessboard(n) {
  let str1 = ' # # # #'
  let str2 = '\n# # # # \n'
  let arr = [str1];
  for (let i = 0; i < n - 1; i++){
   if (arr[i] === str1){
     arr.push(str2);
   } else {
     arr.push(str1)
   }
   
   // console.log(arr.join(''))
  }
   console.log(arr.join(''))
}

