////////////////////////////////////////////////////////////////////////////////

function range(start, end, step=1) {
  let result = []
  if (start === end){
    return result;
  } else if(step < 0){
    return [];
  }

  if (end > start){
    for (let i = start; i <= end; i += step){
      result.push(i);
    }
  }
  if (start > end){
      for (let i = end; i <= start; i -= step){
        result.push(i);
    }
  }
  return result;
}
